<?php
// While & Do While

// While significa ENQUANTO

$contador = 1;

while ($contador <= 10):
    echo "Contador é $contador <br>";
    $contador++;

endwhile;

echo "<hr>";

//Do seginifica: FAÇA

$contador = 1;

do {
    echo "Contador é $contador <br>";
    $contador++;
} while ($contador <= 10);

